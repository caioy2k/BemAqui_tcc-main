const map = L.map("map", {
  zoomControl: true
}).setView([-23.4862, -46.3489], 12);

L.tileLayer("https://{s}.tile.openstreetmap.org/{z}/{x}/{y}.png", {
  maxZoom: 19,
  attribution: "&copy; OpenStreetMap contributors"
}).addTo(map);

const customIcon = L.divIcon({
  className: "bem-aqui-marker",
  html: '<div class="custom-pin"></div>',
  iconSize: [28, 28],
  iconAnchor: [14, 28],
  popupAnchor: [0, -24]
});

const pointsGrid = document.getElementById("pointsGrid");
const chatDrawer = document.getElementById("chatDrawer");
const chatOverlay = document.getElementById("chatOverlay");
const closeChatBtn = document.getElementById("closeChatBtn");
const chatForm = document.getElementById("chatForm");
const chatInput = document.getElementById("chatInput");
const chatMessages = document.getElementById("chatMessages");

const chatPointName = document.getElementById("chatPointName");
const chatPointAddress = document.getElementById("chatPointAddress");
const chatPointDistrict = document.getElementById("chatPointDistrict");
const chatPointHours = document.getElementById("chatPointHours");

let pontoAtual = null;
const bounds = [];

function abrirChat(ponto) {
  pontoAtual = ponto;
  chatPointName.textContent = ponto.nome;
  chatPointAddress.textContent = ponto.endereco;
  chatPointDistrict.textContent = ponto.bairro;
  chatPointHours.textContent = ponto.horario;

  chatMessages.innerHTML = `
    <div class="message message-received">
      Olá! Você está falando com o atendimento do <strong>${ponto.nome}</strong>.
    </div>
    <div class="message message-received">
      Este é um chat visual dentro do site. Depois vocês podem ligar isso ao backend e aos funcionários reais.
    </div>
    <div class="message message-received">
      Como posso ajudar com descarte, horário ou funcionamento deste ponto?
    </div>
  `;

  chatDrawer.classList.add("is-open");
  chatOverlay.classList.add("is-visible");
  chatDrawer.setAttribute("aria-hidden", "false");
  chatInput.focus();
}

function fecharChat() {
  chatDrawer.classList.remove("is-open");
  chatOverlay.classList.remove("is-visible");
  chatDrawer.setAttribute("aria-hidden", "true");
}

function adicionarMensagem(texto, tipo = "sent") {
  const message = document.createElement("div");
  message.className = `message message-${tipo}`;
  message.textContent = texto;
  chatMessages.appendChild(message);
  chatMessages.scrollTop = chatMessages.scrollHeight;
}

function respostaAutomatica(ponto) {
  const respostas = [
    `Recebemos sua mensagem sobre o ${ponto.nome}. Em breve esta área pode ser conectada ao atendimento real.`,
    `Você pode levar recicláveis, móveis desmontados e outros materiais permitidos ao ${ponto.nome}.`,
    `O horário informado para este ponto é ${ponto.horario}.`,
    `Na próxima etapa, este chat pode ser integrado com a equipe do BemAqui e funcionários do ponto.`
  ];

  const resposta = respostas[Math.floor(Math.random() * respostas.length)];

  setTimeout(() => {
    adicionarMensagem(resposta, "received");
  }, 700);
}

pontosColeta.forEach((ponto) => {
  bounds.push([ponto.lat, ponto.lng]);

  const popupHtml = `
    <div class="popup-card">
      <h4>${ponto.nome}</h4>
      <p><strong>Bairro:</strong> ${ponto.bairro}</p>
      <p><strong>Endereço:</strong> ${ponto.endereco}</p>
      <p><strong>Horário:</strong> ${ponto.horario}</p>
      <div class="popup-actions">
        <button class="popup-chat-btn" onclick='abrirChatDoMapa(${JSON.stringify(ponto)})'>
          Falar com o ponto
        </button>
      </div>
    </div>
  `;

  const marker = L.marker([ponto.lat, ponto.lng], { icon: customIcon })
    .addTo(map)
    .bindPopup(popupHtml);

  const card = document.createElement("article");
  card.className = "point-card";
  card.innerHTML = `
    <h4>${ponto.nome}</h4>
    <p>${ponto.endereco}</p>
    <p class="point-meta">${ponto.horario}</p>
    <div class="point-actions">
      <button type="button">Falar com o ponto</button>
    </div>
  `;

  card.addEventListener("click", () => {
    map.setView([ponto.lat, ponto.lng], 15);
    marker.openPopup();
  });

  card.querySelector("button").addEventListener("click", (event) => {
    event.stopPropagation();
    abrirChat(ponto);
  });

  pointsGrid.appendChild(card);
});

if (bounds.length) {
  map.fitBounds(bounds, { padding: [30, 30] });
}

window.abrirChatDoMapa = function (ponto) {
  abrirChat(ponto);
};

closeChatBtn.addEventListener("click", fecharChat);
chatOverlay.addEventListener("click", fecharChat);

chatForm.addEventListener("submit", (event) => {
  event.preventDefault();

  const texto = chatInput.value.trim();
  if (!texto || !pontoAtual) return;

  adicionarMensagem(texto, "sent");
  chatInput.value = "";
  respostaAutomatica(pontoAtual);
});